let angka = 10
var  maju = 20
var mundur = 1

console.log ('Looping maju :\n ')
while (angka <= maju) {
    console.log (angka + ' (Berada pada looping yang maju).'); angka++;
}

console.log ('\n')

angka = 10
console.log ('Looping mundur :\n ')
while (angka >= mundur) {
    console.log (angka + '(berada pada looping yang mundur).'); angka--;
}
