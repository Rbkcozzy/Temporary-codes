let angka = 10
let  maju = 20
let mundur = 1
console.log ('Looping yang maju dari 10 ke 20\n ')
for (angka; angka <= maju; angka++) {
     console.log (angka + ' (Berada pada looping yang maju).');
}

console.log ('\n')
console.log ('Looping yang mundur dari 10 ke 1\n ')

angka = 10 
for (angka; angka >= mundur; angka--) {
    console.log (angka + ' (Berada pada looping yang mundur)')
}